              <p> Una reacción química, también llamada cambio químico o fenómeno químico, es todo proceso termodinámico en el cual dos o más especie químicas o sustancias (llamadas reactantes o reactivos), se transforman, cambiando su estructura molecular y sus enlaces, en otras sustancias llamadas productos.1​ Los reactantes pueden ser elementos o compuestos. Un ejemplo de reacción química es la formación de óxido de hierro producida al reaccionar el oxígeno del aire con el hierro de forma natural, o una cinta de magnesio al colocarla en una llama se convierte en óxido de magnesio, como un ejemplo de reacción inducida. </p>

