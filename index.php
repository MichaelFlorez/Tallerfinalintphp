              <p>Las reacciones químicas ocurren porque las moléculas se están moviendo y cuando se golpean con energía suficiente una contra otras, los enlaces se rompen y los átomos se intercambian para formar nuevas moléculas. También una molécula que está vibrando con energía suficiente puede romperse en moléculas más pequeñas.</p>

